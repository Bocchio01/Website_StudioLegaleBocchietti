<section>
    <h3>Materie Trattate</h3>
    <ul>
        <li>
            <b>Nell'ambito del Diritto Civile</b>: diritto della responsabilità civile per colpa con particolare
            riferimento alla difesa di personale medico e infermieristico e alla difesa di strutture ospedaliere;
            diritto delle assicurazioni anche per infortunistica stradale; diritto delle obbligazioni e dei contratti
            (vendita, appalto, ecc.); diritto delle successioni; diritto dell'esecuzione civile (recupero dei crediti) e
            del fallimento; diritto della proprietà, del condominio e delle locazioni; diritto del lavoro e della
            previdenza sociale; diritto sportivo; diritto bancario; diritto delle procedure cautelari e d'urgenza;
            assistenza e consulenza stragiudiziale.
        </li>
        <li>
            <b>Nell'ambito del Diritto Penale</b>: reato di omicidio colposo e reato di lesioni colpose con particolare
            riferimento alla difesa di personale medico ed infermieristico e alla difesa (quali responsabili civili) di
            strutture ospedaliere; reati in materia aziendale con particolare riferimento ai reati in materia fiscale,
            tutela dell'ambiente (inquinamento), tutela della salute e sicurezza negli ambienti di lavoro (infortuni sul
            lavoro) e tutela della privacy; reati contro la pubblica amministrazione e reato di falso ideologico e
            documentale, circonvenzione di incapace.
        </li>
    </ul>
